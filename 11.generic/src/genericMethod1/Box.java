package genericMethod1;

public class Box {
	private int in;
	private double dou;
	private boolean bo;
	private String str;
	
	private Box(){}
	
	Box(int in) {
		this.in = in;
	}
	
	Box(double dou) {
		this.dou = dou;
	}
	
	Box(boolean bo) {
		this.bo = bo;
	}
	
	Box(String str) {
		this.str = str;
	}
}
